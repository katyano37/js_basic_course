'use strict'

/**
 * функция сложения
 * @param {number} num1
 * @param {number} num2
 * @returns {number} итоговое значение
 */
function digitalSum (num1, num2) {
   return num1 + num2;
}


/**
 * функция вычитания
 * @param {number} num1
 * @param {number} num2
 * @returns {number} итоговое значение
 */
function digitalSubtr (num1, num2) {
   return num1 - num2;
}


/**
 * функция умножения
 * @param {number} num1
 * @param {number} num2
 * @returns {number} итоговое значение
 */
function digitalMult (num1, num2) {
   return num1 * num2;
}

/**
 * функция деления
 * @param {number} num1
 * @param {number} num2
 * @returns {number} итоговое значение
 */
function digitalSep (num1, num2) {
   return num1 / num2;
}
